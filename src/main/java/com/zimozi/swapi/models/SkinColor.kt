package com.zimozi.swapi.models
/**
 * Enumeration of all the character's skin colors.
 */
enum class SkinColor(val rawValue: String) {
    Blue(rawValue = "blue"),
    Brown(rawValue = "brown"),
    BrownMottle(rawValue = "brown mottle"),
    Dark(rawValue = "dark"),
    Fair(rawValue = "fair"),
    Gold(rawValue = "gold"),
    Green(rawValue = "green"),
    GreenTan(rawValue = "green-tan"),
    Grey(rawValue = "grey"),
    Light(rawValue = "light"),
    Metal(rawValue = "metal"),
    MottledGreen(rawValue = "mottled green"),
    None(rawValue = "none"),
    Orange(rawValue = "orange"),
    Pale(rawValue = "pale"),
    Red(rawValue = "red"),
    Silver(rawValue = "silver"),
    Tan(rawValue = "tan"),
    Unknown(rawValue = "unknown"),
    White(rawValue = "white"),
    Yellow(rawValue = "yellow")
}
